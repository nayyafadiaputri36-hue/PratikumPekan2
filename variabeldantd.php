<?php

// Tipe data integer
$angka = 100;
echo " Nilai Integer " . $angka . "<br>";

// Tipe data String
$teks = "Hello, PHP!";
echo "Nilai String:" . $teks . "<br>";

// Tipe data Boolean
$benar = true;
$salah = false;
echo "Nilai Boolean (true): " . ($benar ? 'true' : 'false') . "<br>";
echo "Nilai Boolean (false): " . ($salah ? 'true' : 'false') . "<br>";

// Tipe data float (Double)
$desimal = 10,5;
echo "Nilai Float" : . $desimal . "<br>";

?>
